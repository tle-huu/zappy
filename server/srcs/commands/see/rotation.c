#include "zappy.h"

void		rotation90(int *x, int *y)
{
	*x = *y;
	*y = -1 * *x;
}

void		rotation180(int *x, int *y)
{
	*x *= -1;
	*y *= -1;
}

void		rotation270(int *x, int *y)
{
	*x = -1 * *y;
	*y = *x;
}
